import curses

def main(stdscr):
    curses.curs_set(0)  # –°–∫—Ä—ã–≤–∞–µ–º –∫—É—Ä—Å–æ—Ä
    stdscr.nodelay(1)  # –ù–µ –∂–¥—ë—Ç –Ω–∞–∂–∞—Ç–∏—è, —á—Ç–æ–±—ã –Ω–µ —Ç–æ—Ä–º–æ–∑–∏—Ç—å —Ü–∏–∫–ª
    stdscr.keypad(True)  # –ü–æ–∑–≤–æ–ª—è–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ –∫–ª–∞–≤–∏—à–∏
    stdscr.clear()

    x = 10  # –ù–∞—á–∞–ª—å–Ω–∞—è –ø–æ–∑–∏—Ü–∏—è –ø–æ X
    y = 5   # –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–∞—è –ø–æ–∑–∏—Ü–∏—è –ø–æ Y

    while True:
        stdscr.clear()  # –û—á–∏—â–∞–µ–º —ç–∫—Ä–∞–Ω –ø–µ—Ä–µ–¥ –ø–µ—Ä–µ—Ä–∏—Å–æ–≤–∫–æ–π
        stdscr.addstr(y, x, "üöÄ")  # –†–∏—Å—É–µ–º –æ–±—ä–µ–∫—Ç
        stdscr.refresh()

        key = stdscr.getch()  # –ñ–¥—ë–º –Ω–∞–∂–∞—Ç–∏–µ –∫–ª–∞–≤–∏—à–∏

        if key == ord('q'):  # –í—ã—Ö–æ–¥ –∏–∑ –∏–≥—Ä—ã –ø–æ 'q'
            break
        elif key == ord('a') and x > 0:  # –î–≤–∏–≥–∞–µ–º –≤–ª–µ–≤–æ (–æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ, —á—Ç–æ–±—ã –Ω–µ —É–π—Ç–∏ –≤ -)
            x -= 1
        elif key == ord('d') and x < curses.COLS - 1:  # –î–≤–∏–≥–∞–µ–º –≤–ø—Ä–∞–≤–æ (–ø—Ä–µ–¥–µ–ª –ø–æ —à–∏—Ä–∏–Ω–µ —ç–∫—Ä–∞–Ω–∞)
            x += 1

        curses.napms(20)  # –ó–∞–¥–µ—Ä–∂–∫–∞ –¥–ª—è –ø–ª–∞–≤–Ω–æ—Å—Ç–∏

curses.wrapper(main)
